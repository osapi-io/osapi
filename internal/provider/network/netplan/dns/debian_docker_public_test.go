type DebianDockerPublicTestSuite struct {
	suite.Suite

	logger *slog.Logger
	fs     avfs.VFS
}

func (s *DebianDockerPublicTestSuite) SetupTest() {
	s.logger = slog.New(slog.NewTextHandler(os.Stdout, nil))
	s.fs = memfs.New()
}

func (s *DebianDockerPublicTestSuite) SetupSubTest() {
	s.SetupTest()
}

func (s *DebianDockerPublicTestSuite) TestGetResolvConfByInterface() {
	tests := []struct {
		name          string
		setupFS       func(fs avfs.VFS)
		overrideFS    func() avfs.VFS
		interfaceName string
		want          *dns.GetResult
		wantErr       bool
		errContains   string
	}{
		{
			name: "when resolv.conf has servers search domains and noise",
			setupFS: func(fs avfs.VFS) {
				_ = fs.MkdirAll("/etc", 0o755)
				_ = fs.WriteFile("/etc/resolv.conf", []byte(
					"# Generated by Docker\n"+
						"\n"+
						"nameserver 127.0.0.11\n"+
						"nameserver 8.8.8.8\n"+
						"search example.com local.lan\n"+
						"options ndots:0\n"+
						"malformed\n",
				), 0o644)
			},
			interfaceName: "eth0",
			want: &dns.GetResult{
				DNSServers:    []string{"127.0.0.11", "8.8.8.8"},
				SearchDomains: []string{"example.com", "local.lan"},
			},
		},
		{
			name: "when no search domains defaults to root",
			setupFS: func(fs avfs.VFS) {
				_ = fs.MkdirAll("/etc", 0o755)
				_ = fs.WriteFile("/etc/resolv.conf", []byte(
					"nameserver 8.8.8.8\n",
				), 0o644)
			},
			interfaceName: "eth0",
			want: &dns.GetResult{
				DNSServers:    []string{"8.8.8.8"},
				SearchDomains: []string{"."},
			},
		},
		{
			name: "when multiple search lines uses last one",
			setupFS: func(fs avfs.VFS) {
				_ = fs.MkdirAll("/etc", 0o755)
				_ = fs.WriteFile("/etc/resolv.conf", []byte(
					"nameserver 8.8.8.8\n"+
						"search first.com\n"+
						"search second.com third.com\n",
				), 0o644)
			},
			interfaceName: "eth0",
			want: &dns.GetResult{
				DNSServers:    []string{"8.8.8.8"},
				SearchDomains: []string{"second.com", "third.com"},
			},
		},
		{
			name: "when resolv.conf does not exist",
			setupFS: func(_ avfs.VFS) {
				// Don't create the file
			},
			interfaceName: "eth0",
			wantErr:       true,
			errContains:   "failed to read /etc/resolv.conf",
		},
		{
			name: "when read error during scan",
			overrideFS: func() avfs.VFS {
				base := memfs.New()
				_ = base.MkdirAll("/etc", 0o755)
				_ = base.WriteFile("/etc/resolv.conf", []byte(
					"nameserver 8.8.8.8\n",
				), 0o644)

				ffs := failfs.New(base)
				_ = ffs.SetFailFunc(func(
					_ avfs.VFSBase,
					fn avfs.FnVFS,
					_ *failfs.FailParam,
				) error {
					if fn == avfs.FnFileRead {
						return errors.New("simulated read error")
					}
					return nil
				})

				return ffs
			},
			interfaceName: "eth0",
			wantErr:       true,
			errContains:   "failed to parse /etc/resolv.conf",
		},
	}

	for _, tc := range tests {
		s.Run(tc.name, func() {
			fs := s.fs
			if tc.overrideFS != nil {
				fs = tc.overrideFS()
			} else {
				tc.setupFS(s.fs)
			}

			p := dns.NewDebianDockerProvider(s.logger, fs)
			got, err := p.GetResolvConfByInterface(tc.interfaceName)

			if tc.wantErr {
				s.Error(err)
				s.Contains(err.Error(), tc.errContains)
			} else {
				s.NoError(err)
				s.Equal(tc.want, got)
			}
		})
	}
}

func (s *DebianDockerPublicTestSuite) TestUpdateResolvConfByInterface() {
	tests := []struct {
		name string
	}{
		{
			name: "returns ErrUnsupported for container",
		},
	}

	for _, tt := range tests {
		s.Run(tt.name, func() {
			p := dns.NewDebianDockerProvider(s.logger, s.fs)
			result, err := p.UpdateResolvConfByInterface(
				[]string{"8.8.8.8"},
				[]string{"example.com"},
				"eth0",
				false,
			)

			s.Error(err)
			s.Nil(result)
			s.ErrorIs(err, provider.ErrUnsupported)
		})
	}
}

func (s *DebianDockerPublicTestSuite) TestDeleteNetplanConfig() {
	tests := []struct {
		name string
	}{
		{
			name: "returns ErrUnsupported for container",
		},
	}

	for _, tt := range tests {
		s.Run(tt.name, func() {
			p := dns.NewDebianDockerProvider(s.logger, s.fs)
			changed, err := p.DeleteNetplanConfig("eth0")

			s.Error(err)
			s.False(changed)
			s.ErrorIs(err, provider.ErrUnsupported)
		})
	}
}

func TestDebianDockerPublicTestSuite(t *testing.T) {
	suite.Run(t, new(DebianDockerPublicTestSuite))
}
